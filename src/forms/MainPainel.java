    private void btnMenuHover(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnMenuHover
        // TODO add your handling code here:
        String type = evt.paramString();
        Component element = evt.getComponent();
        
        if(type.indexOf("MOUSE_ENTERED") != -1) {
            element.setBackground(Color.getColor("00183F"));
        }
        if(type.indexOf("MOUSE_EXITED") != -1) {
            element.setBackground(Color.getColor("001233"));
        }
        
    }//GEN-LAST:event_btnMenuHover

    private void btnMenuHideMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnMenuHideMouseClicked
        // TODO add your handling code here:
        int widthNavMenu = 0, widthContent = 0, prefixoNavMenuInicial = 70, prefixoNavMenuFinal = 160;
        if (this.navMenu.getWidth() == prefixoNavMenuFinal) {
            widthNavMenu = prefixoNavMenuInicial;
            widthContent = this.articleContent.getWidth() + prefixoNavMenuFinal;
            
        }
        else {
            widthNavMenu = prefixoNavMenuFinal;
            widthContent = this.articleContent.getWidth() - prefixoNavMenuInicial;
        }
        this.navMenu.setSize(widthNavMenu, this.navMenu.getHeight());
        this.articleContent.setLocation(widthNavMenu, 0);
        this.articleContent.setSize(widthContent, this.articleContent.getHeight());
    }//GEN-LAST:event_btnMenuHideMouseClicked

