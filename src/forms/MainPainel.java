    private void menuItensHover(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_menuItensHover
        String type = evt.paramString();
        Component comp = evt.getComponent();
        
        if(type.indexOf("MOUSE_ENTERED") != -1) {
            comp.setBackground(Color.getColor("#1B3561"));
        }
        
        if(type.indexOf("MOUSE_EXITED") != -1) {
            comp.setBackground(new Color(0, 24, 63));
        }
    }//GEN-LAST:event_menuItensHover

